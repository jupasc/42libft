#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		s;
	int		nb;

	s = 1;
	nb = 0;
	i = 0;
	while (str[i] >= 0 && str[i] <= 32)
		i++;
	if (str[i] == 45)
	{
		s = -1;
		i++;
	}
	else if (str[i] == 43)
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (nb * s);
}
